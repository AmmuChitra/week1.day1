package week1.day1;

public class NumberIsPostiveOrNegative {



	public static void main(String[] args) {
		int Number=35;
		if(Number>0) {
			System.out.println(Number+"Positive number");
			
		}
		else if(Number<0)
		{
			System.out.println(Number+"Negative Number");
			
		}
		else
		{
			System.out.println(Number+"The number is neither positive nor negative");
		}
	}
	

}
